export const getDotsFromMaze = () => {
  const dots = [];
  MAZE.forEach((row, y) => {
    row.forEach((cell, x) => {
      // Collect all 0's as dots, skipping only specific areas if needed
      if (cell === 0) {
        dots.push({ x, y });
      }
    });
  });
  return dots;
};

